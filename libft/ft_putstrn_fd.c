#include "libft.h"

void	ft_putstrn_fd(char *s, int n, int fd)
{
	int		i;

	i = 0;
	if (!s)
		return ;
	while (s[i] && i < n)
	{
		ft_putc_fd(s[i], fd);
		i++;
	}
}
